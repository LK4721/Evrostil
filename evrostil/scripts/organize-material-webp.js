function findSource(file) {
    for (const folder of ["2016-03", "2016-02"]) {
        const candidate = path.join(root, folder, `${file}.webp`);
        if (fs.existsSync(candidate)) return candidate;
    }

    return null;
}

for (const [folder, files] of Object.entries(groups)) {
    const targetFolder = path.join(root, folder);
    fs.mkdirSync(targetFolder, { recursive: true });

    for (const file of files) {
        const source = findSource(file);
        if (!source) {
            console.warn(`Missing: ${file}.webp`);
            continue;
        }

        fs.copyFileSync(source, path.join(targetFolder, `${file}.webp`));
    }

    console.log(`${folder}: ${files.length}`);
}
